package day14;

public class Semo {
	int garo, sero;
	double area;
	public int getGaro() {
		return garo;
	}
	public void setGaro(int garo) {
		this.garo = garo;
	}
	public int getSero() {
		return sero;
	}
	public void setSero(int sero) {
		this.sero = sero;
	}
	public double getArea() {
		return area;
	}
	public void setArea() {
		area = garo*sero*0.5;
	}
	public void setArea(double area) {
		this.area = area;
	}
	public Semo() {
	}


}

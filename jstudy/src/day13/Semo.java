package day13;

public class Semo {
	private int garo, sero;
	private double area;
	public int getWidth() {
		return garo;
	}
	public void setWidth(int width) {
		this.garo = width;
	}
	public int getHeight() {
		return sero;
	}
	public void setHeight(int height) {
		this.sero = height;
	}
	public double getArea() {
		return area;
	}
	public void setArea(double area) {
		this.area = area;
	}
}